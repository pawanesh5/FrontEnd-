function changeTheme(event){
    if(document.getElementById("bright").checked){
        document.body.style.background = 'white';
    }
    if(document.getElementById("cool").checked){
        document.body.style.background = 'rgb(237, 192, 171)';
    }
    if(document.getElementById("fiery").checked){
        document.body.style.background = 'rgb(255, 112, 3)';
    }
    }